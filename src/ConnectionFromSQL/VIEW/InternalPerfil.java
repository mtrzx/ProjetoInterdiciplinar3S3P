    public void listarValores(){
        try {
            UsuarioDAO objUsuarioDAO = new UsuarioDAO();
            DefaultTableModel model = (DefaultTableModel) tabelaPerfil.getModel();
            model.setNumRows (0);
            ArrayList<UsuarioDTO> lista = objUsuarioDAO.PerfilUser();
            
            for(int i=0;i < lista.size(); i++){
                model.addRow (new Object[] {
                    lista.get(i).getId_nome(),
                    lista.get (i).getId_CPF(),
                    lista.get (i).getId_agencia(),
                    lista.get (i).getId_conta(),
                    lista.get (i).getId_email()
                });
            }
            System.out.println(lista.size());
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Listar erro" + erro);
        }
    }
    
